public enum FixtureZone
{
    UpperTraverse,      // Зона верхней траверсы (подвижной)
    LowerBase,         // Зона нижнего основания (неподвижного)
    GripperUpper_Left,      // Внутри верхнего захвата (левый вкладыш)
    GripperUpper_Right,      // Внутри верхнего захвата (правый вкладыш)
    GripperLower_Left,      // Внутри нижнего захвата (левый вкладыш)
    GripperLower_Right,      // Внутри нижнего захвата (правый вкладыш)
    CompressionUpper,  // Зона для верхних плит сжатия
    CompressionLower,  // Зона для нижних плит сжатия
    ProportionalUpperZone, // Зона верхнего гагаринского
    ProportionalLowerZone, // Зона нижнего гагаринского
    PropFixturePoint_UpLeft, // Зона верхнего левого пропорционального вкладыша
    PropFixturePoint_UpRight, // Зона верхнего правого пропорционального вкладыша
    PropFixturePoint_DownLeft, // Зона нижнего левого пропорционального вкладыша
    PropFixturePoint_DownRight, // Зона нижнего правого пропорционального вкладыша
    PropRingUp,         // Зона верхнего пропорционального кольца
    PropRingDown,       // Зона нижнего пропорционального кольца
    None              // Зона не определена или не применима
}
